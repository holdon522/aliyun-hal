void USART3_IRQHandler(void){
	//该函数会清空中断标志，取消中断使能，并间接调用回调函数
	HAL_UART_IRQHandler(&huart3); 
	
//	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_SET);

	if(offset == 200){ //以防溢出
		offset = 0;
	}	

	//将接收到的单个字符扔到自定义字符串缓冲中，形成字符串
	message[offset++] = mesg; 
	
	RX_Flag = 1;

	//实现多次数据返回
	HAL_UART_Receive_IT(&huart3, (uint8_t *)&mesg, 1);
	
}

